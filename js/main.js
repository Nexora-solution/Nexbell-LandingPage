/* ── i18n APPLY ── */
var currentLang = 'en';
function setLang(lang){
  currentLang = lang;
  document.documentElement.lang = lang;
  var tr = T[lang];
  document.querySelectorAll('[data-t]').forEach(function(el){
    var k = el.dataset.t; if(tr[k]!==undefined) el.textContent = tr[k];
  });
  document.querySelectorAll('[data-t-html]').forEach(function(el){
    var k = el.dataset.tHtml; if(tr[k]!==undefined) el.innerHTML = tr[k];
  });
  document.querySelectorAll('.lang-btn').forEach(function(b){
    b.classList.toggle('active', b.dataset.lang === lang);
  });
  document.title = lang==='es'
    ? 'NexBell — Sistema IoT de Videoportería | Nexora'
    : 'NexBell — Intelligent IoT Video Intercom | Nexora';
  buildFaq();
}

/* ── SMOOTH SCROLL ── */
document.addEventListener('click',function(e){
  var a=e.target.closest('a[href^="#"]');
  if(!a) return;
  var t=document.querySelector(a.getAttribute('href'));
  if(t){e.preventDefault();window.scrollTo({top:t.offsetTop,behavior:'smooth'});}
});

/* ── SCROLL REVEAL ── */
(function(){
  var els=document.querySelectorAll('.fade');
  if(!window.IntersectionObserver){els.forEach(function(el){el.style.opacity='1';});return;}
  var io=new IntersectionObserver(function(entries){
    entries.forEach(function(e){
      if(!e.isIntersecting) return;
      var el=e.target, d=+(el.dataset.d||0);
      setTimeout(function(){
        el.classList.add('in');
        setTimeout(function(){el.style.opacity='1';el.classList.remove('fade','in');},820);
      },d);
      io.unobserve(el);
    });
  },{threshold:0.12,rootMargin:'0px 0px -24px 0px'});
  els.forEach(function(el){io.observe(el);});
})();

/* ── RIPPLE ── */
document.addEventListener('click',function(e){
  var el=e.target.closest('.rip');
  if(!el) return;
  var r=el.getBoundingClientRect(),
      sz=Math.max(r.width,r.height)*2.2,
      x=e.clientX-r.left-sz/2, y=e.clientY-r.top-sz/2,
      w=document.createElement('span');
  w.className='rip-w';
  w.style.cssText='width:'+sz+'px;height:'+sz+'px;left:'+x+'px;top:'+y+'px';
  el.appendChild(w);
  setTimeout(function(){w.remove();},620);
});

