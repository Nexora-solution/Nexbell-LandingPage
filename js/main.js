var popup=document.getElementById('tst-popup');
var popText=document.getElementById('tst-popup-text');
var popName=document.getElementById('tst-popup-name');
var popRole=document.getElementById('tst-popup-role');
var popAv=document.getElementById('tst-popup-av');

function openPopup(idx){
  var d=T[currentLang].testimonials[idx];
  if(!d||!popup) return;
  popText.innerHTML=d.text;
  popName.textContent=d.name;
  popRole.textContent=d.role;
  // mirror the avatar image inside the popup
  var av = document.querySelector('.tst-av[data-idx="'+idx+'"] .tst-av-img img');
  if(av && popAv){
    popAv.innerHTML = '<img src="'+av.src+'" alt="'+av.alt+'">';
  }
  popup.classList.add('open');
  document.body.style.overflow='hidden';
}
function closePopup(){
  if(!popup) return;
  popup.classList.remove('open');
  document.body.style.overflow='';
  document.querySelectorAll('.tst-av').forEach(function(a){a.classList.remove('active');});
}

if(popup){
  var closeBtn = document.getElementById('tst-popup-close');
  if(closeBtn) closeBtn.addEventListener('click',closePopup);
  popup.addEventListener('click',function(e){if(e.target===popup) closePopup();});
  document.addEventListener('keydown',function(e){if(e.key==='Escape') closePopup();});
}

document.querySelectorAll('.tst-av').forEach(function(av){
  av.addEventListener('click',function(){
    document.querySelectorAll('.tst-av').forEach(function(a){a.classList.remove('active');});
    av.classList.add('active');
    openPopup(+av.dataset.idx);
  });
});

/* ═══════════════════════════════
   FAQ — accordion
═══════════════════════════════ */
function buildFaq(){
  var list=document.getElementById('faq-list');
  if(!list) return;
  var faqs=T[currentLang].faqs;
  list.innerHTML='';
  faqs.forEach(function(f){
    var item=document.createElement('div');
    item.className='faq-item';
    item.innerHTML=
      '<button class="faq-q" type="button">'+
        '<span>'+f.q+'</span>'+
        '<span class="faq-icon"><span class="material-icons-round">add</span></span>'+
      '</button>'+
      '<div class="faq-a">'+f.a+'</div>';
    list.appendChild(item);
    item.querySelector('.faq-q').addEventListener('click',function(){
      var isOpen=item.classList.contains('open');
      list.querySelectorAll('.faq-item').forEach(function(it){it.classList.remove('open');});
      if(!isOpen) item.classList.add('open');
    });
  });
}

